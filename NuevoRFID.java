    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jTextField2.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Falta Información");
        }
        else{
            String nombre = jTextField2.getText();
            @SuppressWarnings("UnusedAssignment")
            Connection c = null;
            @SuppressWarnings("UnusedAssignment")
            Statement stmt = null;
            try {
                Class.forName("org.postgresql.Driver");
                c = DriverManager
                .getConnection("jdbc:postgresql://localhost:5432/postgres",
                "postgres", "12345");
                c.setAutoCommit(false);
                stmt=c.createStatement();
                String sql = "INSERT INTO rfid (id,nombre) VALUES ('"+RFID+"','"+nombre+"');";
                int executeUpdate = stmt.executeUpdate(sql);
                stmt.close();
                c.commit();
                c.close();
                jTextField2.setText(null);
                JOptionPane.showMessageDialog(this,"Usuario Ingresado con Éxito");
            } catch ( ClassNotFoundException | SQLException e ) {
                System.err.println( e.getClass().getName()+": "+ e.getMessage() );
                JOptionPane.showMessageDialog(this, "USUARIO NO INGRESADO - "+e.getMessage());
                jTextField2.setText(null);
            }
            jButton1.setEnabled(false);
            jLabel2.setEnabled(false);
            jTextField2.setEnabled(false);
            jButton3.setEnabled(true);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

